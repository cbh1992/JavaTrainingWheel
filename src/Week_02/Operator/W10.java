package Week_02.Operator;

public class W10 {
    public static void main(String[] args) {
        System.out.println(3 << 2);
        System.out.println(3 << 1);
        //3을 2진수로 표현하면 11(2)
        //11(2) 을 2자릿수 왼쪽으로 옮겨 1100(2)이 된다
        //1100(2)를 10진법으로 표현하면 12
        //같은 원리로 1자릿수 왼쪽으로 옮기면 110(2), 10진법으로 6
    }
}
