package Week_02.Operator;

public class W03 {
    public static void main(String[] args) {
        int a = 10;
        int b = 9;
        System.out.println(a > b); //a가 b보다 크다
        System.out.println(a < b); //a가 b보다 작다
        System.out.println(a >= b); //a가 b와 같거나 크다
        System.out.println(a <= b); //a가 b와 같거나 작다
        System.out.println(a == b); //a가 b와 같다
        System.out.println(a != b); //a가 b와 다르다
        System.out.println(a == a); //a가 a과 같다
    }
}
