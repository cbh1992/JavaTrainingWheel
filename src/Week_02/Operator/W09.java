package Week_02.Operator;

public class W09 {
    public static void main(String[] args) {
        short x = 10;
        int y = 20;
        int z = x+y;

        long lx = 30L;
        long ly = z + lx;

        float fx = x;
        float fy = y;
        float fz = z;

        System.out.println(z);
        System.out.println(ly);
        System.out.println(fx);
        System.out.println(fy);
        System.out.println(fz);
    }
}
